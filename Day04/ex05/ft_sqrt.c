int	ft_sqrt(int nb)
{
	int a;

	a = nb / 2;
	while (a * a > nb)
	{
		if ((a - 1) * (a - 1) < nb)
			return (0);
		else if (a != 0)
			a--;
		else
			return (0);
	}
	return (a);
}
